#  Write a Python program to demonstrate the creation of variables and different data types.

# integer data type
num = 10
print(num)
print(type(num))

# float data type
num2 = 5.5
print(num2)
print(type(num2))

# character data type 
char = "c"
print(char)
print(type(char))

# string datatype
str = "mayur"
print(str)
print(type(str))

#boolen data type
mayur = True
print(mayur)
print(type(mayur))
