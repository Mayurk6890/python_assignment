#  Write a Python program that manipulates and prints strings using various string methods.

# define string
name = "Mayur kalal"

# convert to uppercase
print(name.upper())

# convert to lowercase
print(name.lower())

# remove leqading and trailing
print(name.strip())

# convert to capitized
print(name.capitalize())

#convert to center
print(name.center())

# find index
print(name.index())
