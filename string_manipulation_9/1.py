#  Write a Python program to demonstrate string slicing.

# Define a string
my_string = "Hello, Mayur"

# Print the original string
print("Original String:", my_string)

# Slice the string from index 0 to 5
print("Slice 1:", my_string[0:5]) 

# Slice the string from index 7 to the end
print("Slice 2:", my_string[7:]) 

# Slice the string from the beginning to index 5
print("Slice 3:", my_string[:5])  

# Slice the string from index 7 to 12
print("Slice 4:", my_string[7:12]) 
 